function modifySDPForrecvonly(sdp) {
  const Lines = sdp.split("\n");
  for (let i = 0; i < Lines.length; i++) {
    if (Lines[i].startsWith("a=sendrecv")) {
      Lines[i] = "a=recvonly"; //modify the sendrcv with recvonly
      break; // only modify the first occurence
    }
  }
  return Lines.join("\n");
}
